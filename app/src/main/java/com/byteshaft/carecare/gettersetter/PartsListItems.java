package com.byteshaft.carecare.gettersetter;

public class PartsListItems {

    private String partName;
    private String partImage;
    private int partId;
    private String partPrice;
    private String partMake;
    private String partModel;
    private String modelYear;
    private String providersContactNumber;

    public String getPartModel() {
        return partModel;
    }

    public void setPartModel(String partModel) {
        this.partModel = partModel;
    }

    public String getModelYear() {
        return modelYear;
    }

    public void setModelYear(String modelYear) {
        this.modelYear = modelYear;
    }



    public String getPartMake() {
        return partMake;
    }

    public void setPartMake(String partMake) {
        this.partMake = partMake;
    }


    public String getPartName() {
        return partName;
    }

    public void setPartName(String partName) {
        this.partName = partName;
    }

    public String getPartImage() {
        return partImage;
    }

    public void setPartImage(String partImage) {
        this.partImage = partImage;
    }

    public int getPartId() {
        return partId;
    }

    public void setPartId(int partId) {
        this.partId = partId;
    }

    public String getPartPrice() {
        return partPrice;
    }

    public void setPartPrice(String partPrice) {
        this.partPrice = partPrice;
    }


    public String getProvidersContactNumber() {
        return providersContactNumber;
    }

    public void setProvidersContactNumber(String providersContactNumber) {
        this.providersContactNumber = providersContactNumber;
    }

}
